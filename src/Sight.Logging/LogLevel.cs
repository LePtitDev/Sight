namespace Sight.Logging
{
    /// <summary>
    /// Define levels of a message
    /// </summary>
    public enum LogLevel
    {
        /// <summary>
        /// Most detailed
        /// </summary>
        Trace,

        /// <summary>
        /// Investigation
        /// </summary>
        Debug,

        /// <summary>
        /// Standard
        /// </summary>
        Information,

        /// <summary>
        /// Warning
        /// </summary>
        Warning,

        /// <summary>
        /// Error
        /// </summary>
        Error,

        /// <summary>
        /// Unhandlable critical error
        /// </summary>
        Critical
    }
}
