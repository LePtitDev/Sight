namespace Sight.Logging.Logs
{
    /// <summary>
    /// Define available color names for logs
    /// </summary>
    public enum LogColors
    {
        /// <summary>
        /// Default foreground color (light for dark theme and black in light theme)
        /// </summary>
        Default,

        /// <summary>
        /// Default foreground with low opacity
        /// </summary>
        LowOpacity,

        /// <summary>
        /// Highlight color
        /// </summary>
        Highlight,

        /// <summary>
        /// Success color
        /// </summary>
        Success,

        /// <summary>
        /// Warning color
        /// </summary>
        Warning,

        /// <summary>
        /// Error color
        /// </summary>
        Error
    }
}
